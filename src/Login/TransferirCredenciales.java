    public void mTabla(){
        DefaultTableModel m1 = new DefaultTableModel();
        m1.addColumn("Usuario");
        m1.addColumn("Nombre");
        tabla1.setModel(m1);
        String datos[] = new String[2];
        try {
            Statement ps = cn.createStatement();
            ResultSet rs = ps.executeQuery("select User_nam,Primer_nombre from Usuarios where Access != 1");
            while (rs.next()) {
                datos[0] = rs.getString(1);
                datos[1] = rs.getString(2);
                m1.addRow(datos);
            }
            tabla1.setModel(m1);
        } catch (SQLException ex) {
            java.util.logging.Logger.getLogger(TransferirCredenciales.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
