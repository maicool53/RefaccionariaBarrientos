    private void transferirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_transferirActionPerformed
        int fila = tabla1.getSelectedRow();
        String usuario = tabla1.getValueAt(fila, 0).toString();
        oculto.setText(usuario);
        if (fila >= 0) {
            try{
                PreparedStatement ut = cn.prepareStatement("update Usuarios set access = 1 where User_nam = ?");
                PreparedStatement utc = cn.prepareStatement("update Usuarios set access = 0 where User_nam = ?");
                ut.setString(1, oculto.getText());
                utc.setString(1, AdministrarCuentas.oculto1.getText());
                ut.executeUpdate();
                utc.executeUpdate();
            }catch (SQLException ex) {
            java.util.logging.Logger.getLogger(TransferirCredenciales.class.getName()).log(Level.SEVERE, null, ex);
        }
        }
        mostrar_pendientes();
        ya_asignados();
        this.dispose();
    }//GEN-LAST:event_transferirActionPerformed

