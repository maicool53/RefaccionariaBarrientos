    private void ButtonAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonAceptarActionPerformed
        if (pass.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Asegurate de introducir tu contraseña :)", "¡Campos vacios!", JOptionPane.WARNING_MESSAGE);
            pass.setBorder(BorderFactory.createMatteBorder(0, 2, 2, 2,  Color.red));
        }else{
            try{
                ResultSet rs = null;
                PreparedStatement ds = cn.prepareStatement("select pass from Usuarios where user_nam = ?");
                ds.setString(1, Loggin.Usuario.getText());
                rs = ds.executeQuery();
                if(rs.next()){
                    PreparedStatement sdd = cn.prepareStatement("update Usuarios set pass = ? where user_nam = ?");
                    sdd.setString(1, ModificarDatosUsuario.pass.getText());
                    sdd.setString(2, Loggin.Usuario.getText());
                    sdd.executeUpdate();
                    JOptionPane.showMessageDialog(null, "Contraseña modificada correctamente :)", "¡Exito!", JOptionPane.INFORMATION_MESSAGE);
                    ModificarDatosUsuario.pass.setText("");
                    ModificarDatosUsuario.pass2.setText(""); 
                    this.dispose();
                }else{
                    JOptionPane.showMessageDialog(null, "Contraseña incorrecta:)", "¡Error!", JOptionPane.ERROR_MESSAGE);
                }
            }catch(SQLException ex){
                System.out.println(ex.getErrorCode());
                System.out.println("Error: "+ex);
            }
        }
    }//GEN-LAST:event_ButtonAceptarActionPerformed

