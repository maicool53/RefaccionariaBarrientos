    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        SS.Clic();
        int fila=tbprovedores.getSelectedRow();
        if(fila>=0){
            try {
                String codpro=tbprovedores.getValueAt(fila,0).toString();
                InputStream dir;
                Map parameter = new HashMap();
                parameter.put("provedor", codpro);
                dir = getClass().getResourceAsStream("/Reportes/ReportProv.jrxml");
                JasperReport reportes=JasperCompileManager.compileReport(dir);
                JasperPrint print=JasperFillManager.fillReport(reportes, parameter,cc.conexion());
                JasperViewer view = new JasperViewer (print, false);
                view.setVisible(true);
            } catch (JRException e) {
                SS.error();
                JOptionPane.showMessageDialog(null, e);
                //System.out.printf(e.getMessage());
            }}else{
                JOptionPane.showMessageDialog(null, "Seleccione algun elemento de la lista");
            }
    }//GEN-LAST:event_jButton1ActionPerformed

